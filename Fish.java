public class Fish {
    private String name;
    private int size;
    private int value;
    
    
    
    public Fish(String name, int size, int value) {
        this.name = name;
        this.size = size;
        this.value = value;
    }   

    public int getValue() {
        return value;
    }

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }

}
